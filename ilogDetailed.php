    <h2>Challenges & Solutions</h2>
    <div class="glass-effect section artifact-section artifact-section-grid artifact-challenges">
        <p>The main issues I had were in the testing phase. As I illustrate in my report,
            the first issue I encountered was that mostly every person tested my prototype on mobile.
            Maze, unfortunately, is not very mobile friendly. I let my testers know to use desktop if possible,
            but it’s just more convenient to test on mobile if that’s how you’re being sent the test.
            One big downside to this is that the user doesn’t know they need to scroll down to see everything,
            and hotspots do not appear if they mis-click. In my next test, I made sure to put some more thought
            into my starting screens to make it more like a process flow versus making them start from
            the beginning every time with no guidance. </p>
        <figure class="figure-row-span">
            <img src="./assets/images/artifact-photos/ffdc-4.png">
            <figcaption>Issues With Testing</figcaption>
        </figure>
        <p>The second issue I encountered was more of an oversight, which is that I wasn't aware that my classmates
            would be the main ones testing my prototype. Originally, I had people in mind who were already familiar with
            the software to avoid the need to explain how to use it. It's reasonable to say my classmates have likely
            never even heard of an ELD prior to this. I did, however; end up sending the test to said people along with
            my classmates.
        </p>
